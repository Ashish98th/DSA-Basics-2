public class else_if_switch_case {
    public static void main(String[] args) {
        int i=7;

        switch(i){

               case 0:
                   System.out.println("zero");
            case 1:
                System.out.println("one");
            case 2:
                System.out.println("two");
            case 7:
                System.out.println("seven");
            case 9:
                System.out.println("Nine");
       }
    }
}
